package com.daxia.model;

public class BookStoreInfo {
    private String storeId;
	private String bookId;
	private String storeCampus;//校区
	private String storePlace;
	private String loginNum;
	private String getNum;
	private String volumnTime;
	private String year;
	private String bookType;
	private String state;

	public String getStoreCampus() {
		return storeCampus;
	}

	public void setStoreCampus(String storeCampus) {
		this.storeCampus = storeCampus;
	}

	public String getStoreId() {
		return storeId;
	}

	public void setStoreId(String storeId) {
		this.storeId = storeId;
	}

	public String getBookId() {
		return bookId;
	}

	public void setBookId(String bookId) {
		this.bookId = bookId;
	}

	public String getStorePlace() {
		return storePlace;
	}

	public void setStorePlace(String storePlace) {
		this.storePlace = storePlace;
	}

	public String getLoginNum() {
		return loginNum;
	}

	public void setLoginNum(String loginNum) {
		this.loginNum = loginNum;
	}

	public String getGetNum() {
		return getNum;
	}

	public void setGetNum(String getNum) {
		this.getNum = getNum;
	}

	public String getVolumnTime() {
		return volumnTime;
	}

	public void setVolumnTime(String volumnTime) {
		this.volumnTime = volumnTime;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getBookType() {
		return bookType;
	}

	public void setBookType(String bookType) {
		this.bookType = bookType;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

}
